#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	void	*org;

	if (!dst && !src)
		return (0);
	org = dst;
	if (src < dst)
	{
		src += len;
		dst += len;
		while (len--)
			*(char *)--dst = *(char *)--src;
	}
	else
		while (len--)
			*(char *)dst++ = *(char *)src++;
	return (org);
}
